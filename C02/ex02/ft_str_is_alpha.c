int	ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (*str <= 64 || *str >= 123)
			return (0);
		else if (*str >= 91 && *str <= 96)
			return (0);
		++str;
	}
	return (1);
}
